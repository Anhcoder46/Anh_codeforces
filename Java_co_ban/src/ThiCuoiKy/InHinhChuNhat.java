package ThiCuoiKy;

public class InHinhChuNhat {
    public static void main(String[] args) {
        int a = 3;
        int b = 8;

        for (int i = 0; i < a; i++) {
            for (int j = 0; j < b; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
